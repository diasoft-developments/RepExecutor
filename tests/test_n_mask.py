"""Тесты числовых масок @n согласно спецификации Diasoft."""
import sys
import os
import pytest

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from parsers.wrd_field_formatter import _apply_single_mask, _parse_n_mask


# --- Тестовые данные по таблице ---
# Формат: (значение, маска, ожидаемый_результат, описание)
test_cases = [
    ("-1.0",      "@n1",        "1",              "Значение -1.0, маска @n1 → 1"),
    ("-1.0",      "@n10",       ".........1",     "Значение -1.0, маска @n10 → заполнение точками + 1"),
    ("-1.0",      "@n-4,1",     "-1,0",           "Значение -1.0, маска @n-4,1 → со знаком минус, запятая разделитель"),
    ("-1.0",      "@n03",       "001",            "Значение -1.0, маска @n03 → заполнение нулями до длины 3"),
    ("1000000.05","@n6",        "******",         "Значение 1000000.05, маска @n6 → переполнение звёздами"),
    # @n12.2: маркер '.', decimals=2, decimal_sep=',', thousands_sep=None
    ("1000000.05","@n12.2",     "1,000,000.05",   "Значение 1000000.05, маска @n12.2 → без разделителей тысяч"),
    # @n12,2.: маркер ',', decimals=2, последний '.' → thousands_sep='.', decimal_sep=','
    ("1000000.05","@n12,2.",    "1.000.000,05",   "Значение 1000000.05, маска @n12,2. → точка как разделитель тысяч"),
    # @n-12.2=: show_sign, маркер '.', decimals=2, последний '=' → thousands_sep='=', decimal_sep=','
    ("1000000.05","@n-12.2=",   "1=000=000.05",   "Значение 1000000.05, маска @n-12.2="),
    # @n12,2_: маркер ',', decimals=2, последний '_' → thousands_sep=' ' (пробел), decimal_sep=','
    ("1000000.05","@n12,2_",    "1 000 000,05",   "Значение 1000000.05, маска @n12,2_ → пробел как разделитель тысяч"),
    ("1000000.05","@n12.2_",    "1 000 000.05",   "Значение 1000000.05, маска @n12.2_ → пробел как разделитель тысяч"),
    # @n9': decimals=0, последний "'" → thousands_sep="'", decimal_sep=','
    ("1000000.05","@n9'",       "1'000'000",      "Значение 1000000.05, маска @n9' → апостроф как разделитель тысяч"),
    # @n_9: no_thousands=True, width=9 → без разделителей
    ("1000000.05","@n_9",       "..1000000",      "Значение 1000000.05, маска @n_9 → без разделителей тысяч"),
    # @n_9<: no_thousands=True, shift='left'
    ("1000000.05","@n_9<",      "1000000",        "Значение 1000000.05, маска @n_9< → прижать влево без разделителей"),
    # @n20.6t: маркер '.', decimals=6, shift='trim', no thousands_sep explicit
    ("1000000.05","@n20.6t",    "1,000,000.050000", "Значение 1000000.05, маска @n20.6t → trim + 6 дробных"),
    ("0",         "@n9",        "........0",      "Значение 0, маска @n9 → заполнение точками + 0"),
    ("0",         "@n9B",       "         ",      "Значение 0, маска @n9B → blank_zero (пробелы)"),
    ("1234.31243","@n4>",       "1243",           "Значение 1234.31243, маска @n4> → последние 4 цифры справа"),
    ("12.234",    "@n4>",       "2234",           "Значение 12.234, маска @n4> → последние 4 цифры справа"),
    ("1.3",       "@n4>",       "0013",           "Значение 1.3, маска @n4> → последние 4 символа с дополнением"),
    # @n20.2_: маркер '.', decimals=2, последний '_' → thousands_sep=' ' (пробел)
    ("1.0",       "@n20.2_",    "1.00",           "@n20.2_ (ширина 20, но decimals>0 → без дополнения)"),
    # @n8.2: маркер '.', decimals=2, no thousands → просто число с запятой
    ("8.5",       "@n8.2",      "8.50",           "@n8.2"),
    # @n8,2: маркер ',', decimals=2, no thousands
    ("8.5",       "@n8,2",      "8,50",           "@n8,2"),
]


@pytest.mark.parametrize("value,mask,expected,description", test_cases)
def test_n_mask(value, mask, expected, description):
    """Проверяет результат применения маски @n к значению."""
    result = _apply_single_mask(str(value), mask)
    print(f"\n{description}")
    print(f"  Значение: '{value}', Маска: '{mask}'")
    print(f"  Ожидаемо: '{expected}' (длина={len(expected)})")
    print(f"  Получено: '{result}' (длина={len(result)})")
    if result != expected:
        # Показать разобранные параметры для отладки
        if mask.lower().startswith('@n'):
            params = _parse_n_mask(mask)
            print(f"  Параметры: {params}")
    assert result == expected, f"Не совпадает: ожидаемо '{expected}', получено '{result}'"


if __name__ == '__main__':
    pytest.main([__file__, '-v'])